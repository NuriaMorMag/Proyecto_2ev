            <p class="date">July 9, 2025</p>
            <p>My brother and I visited a vintage store in Norway. When we walked in,
                the hippie shop assistants told us to take our time. We passed through
                an entrance hall and a kitchen, and to reach the actual shop we had
                to go down a narrow staircase. The store was set up like a basement,
                and it felt a bit creepy. At one point, I even lost sight of my brother,
                which really scared me because he wasn't answering and I couldn't hear anything.</p>
